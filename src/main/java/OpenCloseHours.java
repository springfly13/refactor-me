public class OpenCloseHours {
    private String open ;
    private String close;

    public OpenCloseHours (String openHour, String closeHour){
        this.open = openHour;
        this.close = closeHour;
    }

    public String getOpen() {
        return open;
    }

    public String getClose() {

        return close;
    }
}
